def _parse_post(news_text: str, content: str) -> BlogPost:
    title_match = re.search(r"^제목\s*[:：]\s*(.+)$", content, re.MULTILINE)
    title = title_match.group(1).strip() if title_match else "티스토리 블로그 포스트"

    keywords_match = re.search(r"골드키워드\s*[:：]\*{0,2}\s*(.+)$", content, re.MULTILINE)
    tags = []
    if keywords_match:
        tags = [k.strip() for k in keywords_match.group(1).split(",") if k.strip()][:20]

    slug = re.sub(r"[^\w가-힣\-]", "", title.replace(" ", "-"))[:30] or "post"

    news_stub = NewsItem(
        title=title,
        url="",
        source="수동 입력",
        summary=news_text[:200],
        slug=slug,
        collection_method="manual",
    )

    return BlogPost(
        news_item=news_stub,
        title=title,
        content=content,
        seo_type="tistory_custom",
        tags=tags,
    )


def generate_tistory_post_from_text(news_text: str) -> BlogPost:
    full_content = ""
    stream = stream_completion(
        messages=[
            {"role": "system", "content": SYSTEM_PROMPT},
            {"role": "user", "content": _prompt(news_text)},
        ],
        temperature=0.85,
        max_tokens=14000,
    )
    for chunk in stream:
        delta = chunk.choices[0].delta.content
        if delta:
            full_content += delta

    return _parse_post(news_text, full_content)
